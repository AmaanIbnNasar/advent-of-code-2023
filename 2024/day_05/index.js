export const createOrderingObject = (input) => {
  const ordering = {};
  input.forEach((orderingRule) => {
    const [x, y] = orderingRule.split("|");
    const xParsed = parseInt(x);
    const yParsed = parseInt(y);
    ordering[xParsed] = { before: [], after: [] };
    ordering[yParsed] = { before: [], after: [] };
  });
  input.forEach((orderingRule) => {
    const [x, y] = orderingRule.split("|");
    const xParsed = parseInt(x);
    const yParsed = parseInt(y);

    ordering[xParsed].before.push(yParsed);
    ordering[yParsed].after.push(xParsed);
  });
  return ordering;
};

export const validateUpdate = (update, orderingRules) => {
  return update
    .map((x, i) => {
      if (orderingRules.hasOwnProperty(x)) {
        const { before, after } = orderingRules[x];
        const beforesValid = before.map((num) => {
          const numIndex = update.indexOf(num);
          if (numIndex == -1) {
            return true;
          } else {
            return i < numIndex;
          }
        });
        const aftersValid = after.map((num) => {
          const numIndex = update.indexOf(num);
          if (numIndex == -1) {
            return true;
          } else {
            return i > numIndex;
          }
        });
        const allBeforesValid = beforesValid.reduce(
          (prev, curr) => prev && curr,
          true
        );
        const allAftersValid = aftersValid.reduce(
          (prev, curr) => prev && curr,
          true
        );
        return allBeforesValid && allAftersValid;
      }
    })
    .reduce((prev, curr) => prev && curr);
};

